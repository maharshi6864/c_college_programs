#include <stdio.h>

int main()
{
    float a, b;
    printf("Enter value of a: ");
    scanf("%f", &a);
    printf("Enter value of b: ");
    scanf("%f", &b);
    printf("\n multiply two Floating Point Numbers are >>%.2f \n", a * b);
}